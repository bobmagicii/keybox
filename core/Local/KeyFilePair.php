<?php //////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace Local;

use Nether\Common;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

class KeyFilePair {

	public string
	$Private;

	public string
	$Public;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	static public function
	FromPair(string $Private, string $Public):
	static {

		$Output = new static;
		$Output->Private = $Private;
		$Output->Public = $Public;

		return $Output;
	}

};
